class Calculator {
    addition(firstNumber, secondNumber) {
        return +firstNumber + +secondNumber;
    }

    subtraction(firstNumber, secondNumber) {
        return +firstNumber - +secondNumber;
    }

    division(firstNumber, secondNumber) {
        return +firstNumber / +secondNumber;
    }

    multiplication(firstNumber, secondNumber) {
        return +firstNumber * +secondNumber;  
    }
}